// API to get dashboard data for doctor panel
const doctorDashboard = async (req, res) => {
    try {

        const { docId } = req.body

        const appointments = await appointmentModel.find({ docId })

        let earnings = 0

        appointments.map((item) => {
            if (item.isCompleted || item.payment) {
                earnings += item.amount
            }
        })

        let patients = []

        appointments.map((item) => {
            if (!patients.includes(item.userId)) {
                patients.push(item.userId)
            }
        })



        const dashData = {
            earnings,
            appointments: appointments.length,
            patients: patients.length,
            latestAppointments: appointments.reverse()
        }

        res.json({ success: true, dashData })

    } catch (error) {
        console.log(error)
        res.json({ success: false, message: error.message })
    }
}

// API to send reset password link for Doctor
const forgotPassword = async (req, res) => {
    try {
        const { email } = req.body
        const doctor = await doctorModel.findOne({ email })

        if (!doctor) {
            return res.json({ success: false, message: "Doctor not found" })
        }

        // Generate a token
        const token = jwt.sign({ id: doctor._id }, process.env.JWT_SECRET, { expiresIn: '15m' })

        // Send email
        const transporter = nodemailer.createTransport({
            host: process.env.SMTP_HOST || 'smtp.gmail.com',
            port: process.env.SMTP_PORT || 587,
            secure: false,
            auth: {
                user: process.env.SMTP_USER,
                pass: process.env.SMTP_PASS
            }
        })

        // Admin URL (Doctor logs in via Admin panel on 5173)
        const resetUrl = `${process.env.ADMIN_URL || 'http://localhost:5173'}/reset-password/${token}`

        const mailOptions = {
            from: process.env.SMTP_USER,
            to: email,
            subject: 'Password Reset Request',
            text: `Click the link to reset your password: ${resetUrl}`
        }

        await transporter.sendMail(mailOptions)

        res.json({ success: true, message: "Reset link sent to email" })

    } catch (error) {
        console.log(error)
        res.json({ success: false, message: error.message })
    }
}

// API to reset password for Doctor
const resetPassword = async (req, res) => {
    try {
        const { token, newPassword } = req.body

        const decoded = jwt.verify(token, process.env.JWT_SECRET)
        
        if (!decoded) {
             return res.json({ success: false, message: "Invalid or expired token" })
        }

        const salt = await bcrypt.genSalt(10)
        const hashedPassword = await bcrypt.hash(newPassword, salt)

        await doctorModel.findByIdAndUpdate(decoded.id, { password: hashedPassword })

        res.json({ success: true, message: "Password reset successful" })

    } catch (error) {
        console.log(error)
        res.json({ success: false, message: error.message })
    }
}

